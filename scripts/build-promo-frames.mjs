/**
 * Build the homepage promo frame sequence from the five Seedance journey clips.
 *
 * Usage: node scripts/build-promo-frames.mjs <clipsDir>
 *   <clipsDir> must contain clip1.mp4 ... clip5.mp4 (origin, air, ocean, road, warehouse).
 *
 * Extracts frames at FPS via Playwright's bundled ffmpeg, then writes
 * public/promo/f-NNNN.webp (1600w) and public/promo/sm/f-NNNN.webp (840w)
 * plus src/components/promo/promo-manifest.ts with stage boundaries.
 */
import { execFileSync } from "node:child_process"
import fs from "node:fs"
import path from "node:path"
import sharp from "sharp"

const FPS = 12
const LARGE_W = 1600
const SMALL_W = 840

const clipsDir = process.argv[2]
if (!clipsDir) {
  console.error("usage: node scripts/build-promo-frames.mjs <clipsDir>")
  process.exit(1)
}

const stages = [
  { key: "origin", clip: "clip1.mp4" },
  { key: "air", clip: "clip2.mp4" },
  { key: "ocean", clip: "clip3.mp4" },
  { key: "road", clip: "clip4.mp4" },
  { key: "warehouse", clip: "clip5.mp4" },
]

// Playwright's bundled ffmpeg has no H.264 decoder, so use ffmpeg-static.
const { default: ffmpeg } = await import("ffmpeg-static")
if (!ffmpeg || !fs.existsSync(ffmpeg)) throw new Error("ffmpeg-static not installed (npm i -D ffmpeg-static)")
const rawRoot = path.join(clipsDir, "frames-raw")
const outLarge = path.resolve("public/promo")
const outSmall = path.resolve("public/promo/sm")
fs.rmSync(rawRoot, { recursive: true, force: true })
fs.rmSync(outLarge, { recursive: true, force: true })
fs.mkdirSync(outSmall, { recursive: true })

let globalIndex = 0
const manifestStages = []

for (const stage of stages) {
  const clipPath = path.join(clipsDir, stage.clip)
  if (!fs.existsSync(clipPath)) throw new Error("missing clip: " + clipPath)
  const rawDir = path.join(rawRoot, stage.key)
  fs.mkdirSync(rawDir, { recursive: true })
  execFileSync(ffmpeg, [
    "-hide_banner", "-loglevel", "error",
    "-i", clipPath,
    "-vf", `fps=${FPS}`,
    "-qscale:v", "2",
    path.join(rawDir, "f-%04d.jpg"),
  ])
  const files = fs.readdirSync(rawDir).filter((f) => f.endsWith(".jpg")).sort()
  const start = globalIndex
  for (const file of files) {
    globalIndex += 1
    const src = path.join(rawDir, file)
    const name = `f-${String(globalIndex).padStart(4, "0")}.webp`
    await sharp(src).resize({ width: LARGE_W }).webp({ quality: 58 }).toFile(path.join(outLarge, name))
    await sharp(src).resize({ width: SMALL_W }).webp({ quality: 52 }).toFile(path.join(outSmall, name))
  }
  manifestStages.push({ key: stage.key, start, end: globalIndex - 1 })
  console.log(`${stage.key}: frames ${start}..${globalIndex - 1}`)
}

const manifest = `/** AUTO-GENERATED by scripts/build-promo-frames.mjs — do not edit by hand. */
export const PROMO_FPS = ${FPS}
export const PROMO_FRAME_COUNT = ${globalIndex}
export type PromoStageKey = "origin" | "air" | "ocean" | "road" | "warehouse"
export const promoStageFrames: { key: PromoStageKey; start: number; end: number }[] = ${JSON.stringify(manifestStages, null, 2)}
export function promoFrameSrc(index: number, small: boolean): string {
  const name = "f-" + String(index + 1).padStart(4, "0") + ".webp"
  return (small ? "/promo/sm/" : "/promo/") + name
}
`
fs.mkdirSync(path.resolve("src/components/promo"), { recursive: true })
fs.writeFileSync(path.resolve("src/components/promo/promo-manifest.ts"), manifest)

let totalBytes = 0
for (const f of fs.readdirSync(outLarge)) {
  const p = path.join(outLarge, f)
  if (fs.statSync(p).isFile()) totalBytes += fs.statSync(p).size
}
console.log(`total frames: ${globalIndex}, large set ~${(totalBytes / 1024 / 1024).toFixed(1)} MB`)
